package basic.question01;

/**
 * 第4章 変数
 */

public class Question01_1 {

	public static void main(String[] args) {

		//１．intがたの変数[price]を初期化して作成する。
		int price = 120;

		//２．Stringがたの変数[item]を初期化して作成する。
		String item = "ノート";

		//３．[price、[item]の順番で値を次の通りコンソールに出力する。
		System.out.println(price);
		System.out.println(item + "\n");

		//４．[price]に値を代入する。
		price = 200;

		//５．[item]に値を代入する。
		item = "鉛筆";

		//６．[price]、[item]の順番で値を次の通りコンソールに出力する。
		System.out.println(price);
		System.out.println(item);

	}

}
